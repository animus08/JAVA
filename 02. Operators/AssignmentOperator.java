/* Assignment Operator Example... */
class AssignmentOperator {
    public static void main(String[] args) {
        int a;
        // Simple
        a = 10;
        System.out.println(a);
        // Compound (a=a+10)
        a += 10;
        System.out.println(a);
        // Compound (a=a-10)
        a -= 10;
        System.out.println(a);
    }
}